int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb < 0)
		return (0);
	else
	{
		while (i <= 46340 && i < nb)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
